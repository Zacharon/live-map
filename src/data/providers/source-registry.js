export function providersForDomain(domainId) {
  return PROVIDER_SOURCE_REGISTRY.filter((provider) => provider.domains.includes(domainId));
}

export function domainSourceStatus(domainId) {
  const row = DOMAIN_SOURCE_STATUS.find(([id]) => id === domainId);
  return row ? { domainId: row[0], status: row[1], message: row[2] } : { domainId, status: "unknown", message: "Source status is not registered." };
}

