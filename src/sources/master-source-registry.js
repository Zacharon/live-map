export const MASTER_SOURCE_REGISTRY = [...structuredSources, ...compactSources.map(compactToSource)].sort((a, b) => a.domain.localeCompare(b.domain) || a.name.localeCompare(b.name));

export function sourceRegistryStats(sources = MASTER_SOURCE_REGISTRY) {
  const countBy = (key) => sources.reduce((acc, sourceRecord) => {
    const value = sourceRecord[key] || "unknown";
    acc[value] = (acc[value] || 0) + 1;
    return acc;
  }, {});
  return {
    total: sources.length,
    domains: countBy("domain"),
    accessModes: countBy("accessMode"),
    statuses: countBy("status"),
    sourceTiers: countBy("sourceTier"),
    live: sources.filter((sourceRecord) => sourceRecord.status === "live").length,
    implemented: sources.filter((sourceRecord) => sourceRecord.implemented).length,
    openOrPublic: sources.filter((sourceRecord) => ["open-api", "public-rss", "open-bulk-data", "static-geospatial"].includes(sourceRecord.accessMode)).length,
    licensedOrCredentialed: sources.filter((sourceRecord) => ["credential-required", "commercial-license", "registration-required"].includes(sourceRecord.accessMode)).length,
    linkOnlyOrDisabled: sources.filter((sourceRecord) => ["link-only", "disabled", "license-required", "authentication-required"].includes(sourceRecord.status)).length,
    legalReviewRequired: sources.filter((sourceRecord) => sourceRecord.legalReviewRequired).length,
  };
}

export function validateSourceRecord(sourceRecord) {
  const errors = [];
  for (const key of ["id", "name", "description", "domain", "category", "accessMode", "status", "sourceTier", "verificationState", "sourceUrl", "attribution"]) {
    if (!sourceRecord[key]) errors.push(`${sourceRecord.id || "unknown"} missing ${key}`);
  }
  if (!SOURCE_DOMAINS.includes(sourceRecord.domain)) errors.push(`${sourceRecord.id} has unknown domain ${sourceRecord.domain}`);
  if (!SOURCE_ACCESS_CLASSIFICATIONS.includes(sourceRecord.accessMode)) errors.push(`${sourceRecord.id} has unknown access mode ${sourceRecord.accessMode}`);
  if (!IMPLEMENTATION_STATUSES.includes(sourceRecord.status)) errors.push(`${sourceRecord.id} has unknown status ${sourceRecord.status}`);
  if (!SOURCE_QUALITY_TIERS.includes(sourceRecord.sourceTier)) errors.push(`${sourceRecord.id} has unknown source tier ${sourceRecord.sourceTier}`);
  if (!VERIFICATION_STATES.includes(sourceRecord.verificationState)) errors.push(`${sourceRecord.id} has unknown verification state ${sourceRecord.verificationState}`);
  if (sourceRecord.status === "live" && !sourceRecord.implemented) errors.push(`${sourceRecord.id} is live but not implemented`);
  if (sourceRecord.status === "live" && sourceRecord.legalReviewRequired) errors.push(`${sourceRecord.id} is live but still needs legal review`);
  if (sourceRecord.accessMode === "prohibited-or-unclear" && sourceRecord.status !== "disabled") errors.push(`${sourceRecord.id} prohibited source must be disabled`);
  if (sourceRecord.status === "link-only" && sourceRecord.implemented) errors.push(`${sourceRecord.id} link-only source cannot be implemented`);
  if (!/^https:\/\//.test(sourceRecord.sourceUrl)) errors.push(`${sourceRecord.id} sourceUrl must be HTTPS`);
  for (const envVar of sourceRecord.environmentVariables || []) {
    if (!/^[A-Z][A-Z0-9_]+$/.test(envVar)) errors.push(`${sourceRecord.id} invalid env var ${envVar}`);
  }
  return errors;
}

export function validateSourceRegistry(sources = MASTER_SOURCE_REGISTRY) {
  const errors = [];
  const ids = new Set();
  const urls = new Set();
  for (const sourceRecord of sources) {
    errors.push(...validateSourceRecord(sourceRecord));
    if (ids.has(sourceRecord.id)) errors.push(`duplicate source id ${sourceRecord.id}`);
    ids.add(sourceRecord.id);
    if (urls.has(sourceRecord.sourceUrl)) errors.push(`duplicate source URL ${sourceRecord.sourceUrl}`);
    urls.add(sourceRecord.sourceUrl);
  }
  return { valid: errors.length === 0, errors };
}

export function filterSources(sources = MASTER_SOURCE_REGISTRY, filters = {}) {
  const query = String(filters.q || filters.query || "").trim().toLowerCase();
  return sources.filter((sourceRecord) => {
    if (query) {
      const haystack = [sourceRecord.name, sourceRecord.description, sourceRecord.domain, sourceRecord.category, sourceRecord.type, sourceRecord.bestUse, sourceRecord.sourceUrl].join(" ").toLowerCase();
      if (!haystack.includes(query)) return false;
    }
    for (const key of ["domain", "category", "accessMode", "status", "sourceTier"]) {
      if (filters[key] && sourceRecord[key] !== filters[key]) return false;
    }
    if (filters.official === "true" && !sourceRecord.official) return false;
    if (filters.credentialRequired === "true" && !sourceRecord.credentialRequired) return false;
    if (filters.implemented === "true" && !sourceRecord.implemented) return false;
    if (filters.legalReviewRequired === "false" && sourceRecord.legalReviewRequired) return false;
    return true;
  });
}

export function sourceById(id, sources = MASTER_SOURCE_REGISTRY) {
  return sources.find((sourceRecord) => sourceRecord.id === id) || null;
}
