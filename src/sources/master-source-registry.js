function source(overrides) {
  const accessMode = overrides.accessMode || "link-only";
  const status = overrides.status || (["commercial-license"].includes(accessMode) ? "license-required" : accessMode === "credential-required" ? "authentication-required" : accessMode === "prohibited-or-unclear" ? "disabled" : "planned");
  const licenseKnown = overrides.commercialUse !== "unknown" && overrides.redistribution !== "unknown";
  return {
    id: overrides.id,
    name: overrides.name,
    description: overrides.description,
    osintCategory: overrides.osintCategory || overrides.category,
    domain: overrides.domain,
    category: overrides.category,
    type: overrides.type || overrides.category,
    bestUse: overrides.bestUse || "Research, verification, event discovery, or source attribution.",
    input: overrides.input || "server-side provider adapter",
    output: overrides.output || "metadata, alerts, event records, or reference layers",
    visualizationTypes: overrides.visualizationTypes || ["card-feed", "source-provenance"],
    official: Boolean(overrides.official),
    sourceTier: overrides.sourceTier || (overrides.official ? "tier-1-primary-official" : "tier-3-reputable-reporting"),
    accessMode,
    status,
    verificationState: overrides.verificationState || (overrides.official ? "primary-confirmed" : "reported"),
    apiAvailable: Boolean(overrides.apiAvailable),
    rssAvailable: Boolean(overrides.rssAvailable),
    bulkDataAvailable: Boolean(overrides.bulkDataAvailable),
    credentialRequired: Boolean(overrides.credentialRequired || accessMode === "credential-required" || accessMode === "commercial-license"),
    registrationRequired: Boolean(overrides.registrationRequired || accessMode === "registration-required"),
    pricing: overrides.pricing || (accessMode === "commercial-license" ? "paid or licensed" : "free or unknown"),
    commercialUse: overrides.commercialUse || "unknown",
    redistribution: overrides.redistribution || "unknown",
    caching: overrides.caching || (licenseKnown ? "metadata cache permitted when source terms allow" : "disabled until terms review"),
    retention: overrides.retention || (licenseKnown ? "store normalized metadata; follow provider terms for raw records" : "do not store raw records until reviewed"),
    attribution: overrides.attribution || overrides.name,
    attributionText: overrides.attributionText || overrides.attribution || overrides.name,
    termsUrl: overrides.termsUrl || null,
    docsUrl: overrides.docsUrl || overrides.sourceUrl || null,
    sourceUrl: overrides.sourceUrl,
    coverage: overrides.coverage || "global or provider-specific",
    languages: overrides.languages || ["en"],
    historicalCoverage: overrides.historicalCoverage || "provider-defined",
    refreshGuidance: overrides.refreshGuidance || "manual review before polling",
    suggestedRefreshIntervalMs: overrides.suggestedRefreshIntervalMs || null,
    rateLimitGuidance: overrides.rateLimitGuidance || "central server-side polling with cache and backoff",
    personalDataRisk: overrides.personalDataRisk || "low",
    securityRisk: overrides.securityRisk || "low",
    opsecMode: overrides.opsecMode || "passive-server-side",
    legalReviewRequired: overrides.legalReviewRequired ?? (!licenseKnown || accessMode === "commercial-license" || accessMode === "prohibited-or-unclear"),
    lastTermsReview: overrides.lastTermsReview || null,
    termsReviewedAt: overrides.termsReviewedAt || overrides.lastTermsReview || null,
    lastTechnicalVerification: overrides.lastTechnicalVerification || null,
    adapterId: overrides.adapterId || null,
    environmentVariables: overrides.environmentVariables || [],
    implemented: Boolean(overrides.implemented || status === "live" || status === "partial" || status === "degraded" || status === "delayed"),
    knownLimitations: overrides.knownLimitations || [],
    disableReason: overrides.disableReason || (status === "disabled" ? "Disabled until access, terms, and redistribution are reviewed." : null),
    sourceFreshness: overrides.sourceFreshness || "provider-defined",
    sourceStatusPolicy: overrides.sourceStatusPolicy || "provider failures must be surfaced separately from event freshness",
    cachePolicy: overrides.cachePolicy || "cache server-side only; never fan out browser requests",
    cachingAllowed: overrides.cachingAllowed ?? Boolean(licenseKnown),
    takedownPolicy: overrides.takedownPolicy || "honor correction, takedown, and retraction requests according to policy",
    notes: overrides.notes || "",
  };
}

